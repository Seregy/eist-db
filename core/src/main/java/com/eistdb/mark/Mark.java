package com.eistdb.mark;

public class Mark {
    private Long id;
    private String name;

    public Mark() {

    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getMark() {
        return name;
    }

    public void setMark(String name) {
        this.name = name;
    }
}
